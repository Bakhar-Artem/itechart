package by.bakhar.task1.exception;

public class DoubleArrayReaderException extends Exception {
    public DoubleArrayReaderException() {
    }

    public DoubleArrayReaderException(String message) {
        super(message);
    }

    public DoubleArrayReaderException(String message, Throwable cause) {
        super(message, cause);
    }

    public DoubleArrayReaderException(Throwable cause) {
        super(cause);
    }
}
